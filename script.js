        /* =========================
           VARIABLES
        ========================== */
        let questions = questionsFR;
        let index = 0;
        let score = 0;
        let debut;
        let timerInterval;

        /* =========================
           TIMER
        ========================== */
        function lancerTimer() {
            debut = Date.now();
            clearInterval(timerInterval);

            timerInterval = setInterval(() => {
                let temps = Math.floor((Date.now() - debut) / 1000);
                document.getElementById("timer").textContent = "Temps : " + temps + " s";
            }, 1000);
        }

        /* =========================
           AFFICHER QUESTION
        ========================== */
        function afficherQuestion() {
            let q = questions[index];
            document.getElementById("question").textContent = q.question;

            let html = "";
            q.choix.forEach((choix, i) => {
                html += `
                    <input type="radio" name="reponse" value="${i}">
                    ${choix}<br>
                `;
            });

            document.getElementById("reponses").innerHTML = html;
        }

        /* =========================
           QUESTION SUIVANTE
        ========================== */
        function suivant() {
            let selection = document.querySelector('input[name="reponse"]:checked');

            if (!selection) {
                alert("Choisis une réponse !");
                return;
            }

            if (parseInt(selection.value) === questions[index].bonne) {
                score++;
            }

            index++;

            if (index < questions.length) {
                afficherQuestion();
            } else {
                finQuiz();
            }
        }

        /* =========================
           FIN DU QUIZ
        ========================== */
        function finQuiz() {
            clearInterval(timerInterval);
            let tempsFinal = Math.floor((Date.now() - debut) / 1000);

            document.getElementById("quiz").style.display = "none";
            document.getElementById("resultat").innerHTML =
                `🔥 Score : ${score} / ${questions.length}<br>
                 ⏱ Temps : ${tempsFinal} secondes`;
        }

        /* =========================
           LANGUE - ACCUEIL
        ========================== */
        function changerLangueAccueil(langue) {
            if (langue === "fr") {
                document.getElementById("titre").textContent = "Test le quiz pour voir si t'es un vrai fan de star wars.";
                document.getElementById("soustitre").textContent = "Attention il y a un timer qui commence dès que tu appuies sur ce bouton.";
                document.getElementById("btntext").textContent = "commencer le quiz";
            } if (langue === "en") {
                document.getElementById("titre").textContent = "Test the quiz to see if you are a true Star Wars fan.";
                document.getElementById("soustitre").textContent = "Warning there is a timer that starts as soon as you press this button.";
                document.getElementById("btntext").textContent = "start the quiz";
            }
        }

        /* =========================
           LANGUE - QUIZ
        ========================== */
        function changerLangue(langue) {
            questions = langue === "fr" ? [...questionsFR] : [...questionsEN];
            melangerQuestions(questions);
            index = 0;
            score = 0;

            document.getElementById("quiz").style.display = "block";
            document.getElementById("resultat").textContent = "";

            lancerTimer();
            afficherQuestion();
        }

        function melangerQuestions(tableau) {
            for (let i = tableau.length - 1; i > 0; i--) {
                let j = Math.floor(Math.random() * (i + 1));
                [tableau[i], tableau[j]] = [tableau[j], tableau[i]];
            }
        }

        /* =========================
           LANCEMENT INITIAL
        ========================== */
        changerLangue("fr");